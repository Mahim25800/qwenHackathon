"""
DatasetProfile schema — output of the Data Explorer Agent.
Captures the characteristics and theoretical bounds of the target dataset.
"""

from pydantic import BaseModel, Field
from typing import List, Optional


class TheoreticalBounds(BaseModel):
    """Computed theoretical constraints based on input dimensions."""

    max_pooling_layers: int = Field(
        description="Maximum number of 2x2 pooling layers before spatial dims reach 1x1"
    )
    min_receptive_field: int = Field(
        description="Minimum receptive field size needed to capture meaningful features"
    )
    max_conv_depth: int = Field(
        description="Maximum reasonable number of convolutional layers"
    )
    spatial_dims_after_pools: List[int] = Field(
        default_factory=list,
        description="Spatial dimensions after each pooling layer"
    )


class DatasetProfile(BaseModel):
    """
    Complete profile of a dataset generated by the Data Explorer agent.
    Contains both metadata and computed theoretical bounds to guide architecture design.
    """

    name: str = Field(description="Dataset identifier, e.g. 'CIFAR-10'")
    input_shape: List[int] = Field(
        description="Input tensor shape as [channels, height, width], e.g. [3, 32, 32]"
    )
    num_classes: int = Field(description="Number of output classes")
    num_samples: int = Field(
        default=50000,
        description="Approximate number of training samples"
    )
    data_characteristics: str = Field(
        description="Human-readable description: complexity, color space, texture patterns"
    )
    theoretical_bounds: TheoreticalBounds = Field(
        description="Computed architectural constraints based on input dimensions"
    )
    recommended_architectures: List[str] = Field(
        description="List of architecture families suitable for this data profile, e.g. ['ResNet', 'MobileNet', 'EfficientNet']"
    )
    complexity_score: float = Field(
        default=0.5,
        ge=0.0,
        le=1.0,
        description="Estimated task complexity from 0.0 (trivial) to 1.0 (extremely hard)"
    )
    notes: Optional[str] = Field(
        default=None,
        description="Additional observations or warnings about the dataset"
    )
